    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String rw[]=dataset.trim().split("\n");
        
        String metadata="@relation XXXXDataset\n" +
"\n" +
"@attribute Children_Id real\n" +
"@attribute Gender real\n" +
"@attribute Age real\n" +
"@attribute Have_a_migraine real\n" +
"@attribute BPRS_score real\n" +
"@attribute Do_use_clozapine real\n" +
"@attribute Do_use_traditional_neuroleptic real\n" +
"@attribute Do_use_mood_stabilizing_medication real\n" +
"@attribute EEG real\n" +
"@attribute Target {RSz, TD}\n" +
"\n" +
"@data";
        
        String trD="";
        for(int i=1;i<(rw.length*3/4);i++)
        {
            trD=trD+rw[i].trim()+"\n";
        }
        String trainingDataset=metadata.replaceAll("XXXX","Training")+"\n"+trD.substring(0,trD.lastIndexOf('\n'));
        filewrite("TrainingDataset.arff",trainingDataset);
        jTextArea1.append("=====================================================================================\n");
        jTextArea1.append("                                           Training Dataset\n");
        jTextArea1.append("=====================================================================================\n");
        jTextArea1.append(trainingDataset.trim()+"\n");
        
        String teD="";
        for(int i=(rw.length*3/4);i<rw.length;i++)
        {
            String te=rw[i].substring(0,rw[i].lastIndexOf(','));
            teD=teD+te.trim()+",?\n";
        }
        String testingDataset=metadata.replaceAll("XXXX","Testing")+"\n"+teD.substring(0,teD.lastIndexOf('\n'));
        filewrite("TestingDataset.arff",testingDataset);
        jTextArea1.append("=====================================================================================\n");
        jTextArea1.append("                                           Testing Dataset\n");
        jTextArea1.append("=====================================================================================\n");
        jTextArea1.append(testingDataset.trim()+"\n");
        
    }//GEN-LAST:event_jButton1ActionPerformed

