    private void filewrite(String filename, String filecontent) {
        try
        {            
            File fe=new File(filename);
            String sg=filecontent.trim();
            FileOutputStream fos=new FileOutputStream(fe);
            fos.write(sg.getBytes());
            fos.close();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
